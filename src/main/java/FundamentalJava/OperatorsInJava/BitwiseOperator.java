package FundamentalJava.OperatorsInJava;

public class BitwiseOperator
{
    public static void main(String[] args)
    {
        int mybitwiseXvalue=130;
        int mybitwiseYvalue=115;
        System.out.println("my x value is "+mybitwiseXvalue+"\n"+"my Y value is "+mybitwiseYvalue+"\n"+" this two number bitwise AND & operator value is "+(mybitwiseXvalue&mybitwiseYvalue));
        System.out.println("my x value is "+mybitwiseXvalue+"\n"+"my Y value is "+mybitwiseYvalue+"\n"+" this two number bitwise Inclusive OR  | operator value is "+(mybitwiseXvalue|mybitwiseYvalue));
        System.out.println("my x value is "+mybitwiseXvalue+"\n"+"my Y value is "+mybitwiseYvalue+"\n"+" this two number bitwise Exclusive OR ^ operator value is "+(mybitwiseXvalue^mybitwiseYvalue));


    }
}
