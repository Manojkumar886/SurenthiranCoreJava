package FundamentalJava.OperatorsInJava;

public class ShiftOperator
{
    public static void main(String[] args)
    {
        int myfirstvalue=100;
        int mysecondvalue=5;
        System.out.println(myfirstvalue >> mysecondvalue);//  a/2^b  =3.12
        System.out.println(myfirstvalue << mysecondvalue);//  a* 2^b =3200
    }
}
